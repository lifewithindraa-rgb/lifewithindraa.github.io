// Language Manager
class LanguageManager {
    constructor() {
        this.currentLang = localStorage.getItem('lang') || 'en';
        this.init();
    }

    init() {
        this.updatePage();
        this.updateSwitcher();
    }

    setLanguage(lang) {
        this.currentLang = lang;
        localStorage.setItem('lang', lang);
        this.updatePage();
        this.updateSwitcher();
    }

    toggleLanguage() {
        const newLang = this.currentLang === 'en' ? 'id' : 'en';
        this.setLanguage(newLang);
    }

    updatePage() {
        const t = translations[this.currentLang];
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (t[key]) {
                if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                    el.placeholder = t[key];
                } else {
                    el.textContent = t[key];
                }
            }
        });
        document.documentElement.lang = this.currentLang;
    }

    updateSwitcher() {
        const t = translations[this.currentLang];
        const switcher = document.getElementById('lang-switch');
        const switcherMobile = document.getElementById('lang-switch-mobile');
        if (switcher) switcher.textContent = t.lang_switch;
        if (switcherMobile) switcherMobile.textContent = t.lang_switch;
    }
}

// Initialize after DOM is ready
let langManager;

document.addEventListener('DOMContentLoaded', () => {
    langManager = new LanguageManager();

    // Desktop language switcher
    const langSwitch = document.getElementById('lang-switch');
    if (langSwitch) {
        langSwitch.addEventListener('click', () => langManager.toggleLanguage());
    }

    // Mobile language switcher
    const langSwitchMobile = document.getElementById('lang-switch-mobile');
    if (langSwitchMobile) {
        langSwitchMobile.addEventListener('click', () => langManager.toggleLanguage());
    }
});
